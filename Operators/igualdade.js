console.log( 1 === 1 ) //Ele além de calcular os numeros tambem compara os tipos (Trara true)
console.log( '1' === 1 ) //Ele além de calcular os numeros tambem compara os tipos (Trara falso)

// Igualdade solta
console.log( 1 == 1 );  //Ele apenas compara os numeros (Trara true)
console.log( 1 == '1' ); //Como ele não se importa com o tipo ele passara a string para númerico 