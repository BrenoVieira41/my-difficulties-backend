let salario = 100;

console.log(salario + salario); // Mais == 200
console.log(salario - salario); // Menos == 0
console.log(salario * salario); // Multiplicação == 10000
console.log(salario / salario); // Dividido == 1
console.log(5 ** 5); // Exponencial == 3125

let idade = 18; 

console.log(idade ++) // Incremento (Por estar apos a variavel ele so mudara após então não acrecentara agora) == 18
console.log(++ idade) // Incremento (Por ser passado antes ele ja adicionara agora a informação) == 20 (por causa da chamada a cima)

console.log(idade --) // Decremento (Por estar apos a variavel ele so mudara após então não acrecentara agora) == 20
console.log(-- idade) // Decremento (Por ser passado antes ele ja adicionara agora a informação) == 18 (por causa da chamada a cima)